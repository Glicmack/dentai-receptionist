interface AdminSidebarProps {
  open?: boolean
  onClose?: () => void
}

export function AdminSidebar({ open, onClose }: AdminSidebarProps) {
  const pathname = usePathname()
  const router = useRouter()
  const supabase = createClient()
  const [userEmail, setUserEmail] = useState<string | null>(null)

  useEffect(() => {
    supabase.auth.getUser().then(({ data: { user } }) => {
      setUserEmail(user?.email ?? null)
    })
  }, [supabase])

  async function handleLogout() {
    await supabase.auth.signOut()
    router.push("/login")
    router.refresh()
  }

  return (
    <>
      {open && (
        <div
          className="fixed inset-0 z-40 bg-black/50 backdrop-blur-sm md:hidden"
          onClick={onClose}
        />
      )}

      <aside
        className={cn(
          "fixed inset-y-0 left-0 z-50 flex h-screen w-64 flex-col border-r bg-gray-950 text-gray-100 transition-transform duration-200 ease-in-out md:static md:translate-x-0",
          open ? "translate-x-0" : "-translate-x-full"
        )}
      >
        {/* Logo */}
        <div className="flex h-16 items-center justify-between border-b border-gray-800 px-6">
          <Link href="/admin" className="flex items-center gap-2">
            <div className="flex h-8 w-8 items-center justify-center rounded-lg bg-gradient-to-br from-purple-500 to-purple-700 shadow-md">
              <span className="text-sm font-bold text-white">G</span>
            </div>
            <div>
              <span className="text-lg font-bold text-white">GOD Panel</span>
              <p className="text-[10px] text-gray-400 leading-none">Platform Admin</p>
            </div>
          </Link>
          <button
            onClick={onClose}
            className="rounded-lg p-1 text-gray-400 hover:bg-gray-800 md:hidden"
          >
            <svg className="h-5 w-5" fill="none" viewBox="0 0 24 24" stroke="currentColor" strokeWidth={2}>
              <path strokeLinecap="round" strokeLinejoin="round" d="M6 18L18 6M6 6l12 12" />
            </svg>
          </button>
        </div>

        {/* Navigation */}
        <nav className="flex-1 space-y-1 p-3 overflow-y-auto">
          {NAV_ITEMS.map((item) => {
            const isActive = pathname === item.href ||
              (item.href !== "/admin" && pathname.startsWith(item.href))
            return (
              <Link
                key={item.href}
                href={item.href}
                onClick={onClose}
                className={cn(
                  "relative flex items-center gap-3 rounded-lg px-3 py-2.5 text-sm font-medium transition-all duration-200",
                  isActive
                    ? "bg-purple-500/20 text-purple-300"
                    : "text-gray-400 hover:bg-gray-800 hover:text-gray-200"
                )}
              >
                {isActive && (
                  <div className="absolute left-0 top-1/2 h-6 w-1 -translate-y-1/2 rounded-r-full bg-purple-500" />
                )}
                {item.icon}
                {item.label}
              </Link>
            )
          })}
        </nav>

        {/* Quick access to Doctor Panel */}
        <div className="p-3">
          <Link href="/dashboard">
            <div className="rounded-xl bg-gray-800/50 border border-gray-700 p-3 hover:bg-gray-800 transition-colors cursor-pointer">
              <p className="text-xs font-medium text-gray-300">Switch to Doctor View</p>
              <p className="text-[10px] text-gray-500 mt-0.5">Go to clinic dashboard</p>
            </div>
          </Link>
        </div>

        {/* User info & Logout */}
        <div className="border-t border-gray-800 p-3">
          {userEmail && (
            <div className="mb-2 flex items-center gap-2 rounded-lg px-3 py-2">
              <div className="flex h-8 w-8 shrink-0 items-center justify-center rounded-full bg-purple-500/20 text-xs font-bold text-purple-300">
                {userEmail[0].toUpperCase()}
              </div>
              <span className="truncate text-xs text-gray-400" title={userEmail}>
                {userEmail}
              </span>
            </div>
          )}
          <Button
            variant="ghost"
            className="w-full justify-start text-gray-400 hover:text-gray-200 hover:bg-gray-800"
            onClick={handleLogout}
          >
            <svg className="mr-3 h-5 w-5" fill="none" viewBox="0 0 24 24" stroke="currentColor" strokeWidth={1.5}>
              <path strokeLinecap="round" strokeLinejoin="round" d="M15.75 9V5.25A2.25 2.25 0 0013.5 3h-6a2.25 2.25 0 00-2.25 2.25v13.5A2.25 2.25 0 007.5 21h6a2.25 2.25 0 002.25-2.25V15m3 0l3-3m0 0l-3-3m3 3H9" />
            </svg>
            Log Out
          </Button>
        </div>
      </aside>
    </>
  )
}
